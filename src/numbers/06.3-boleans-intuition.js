let boolCheck = cond => ifTrue => ifFalse => cond ? ifTrue : ifFalse

let TRUE = boolCheck(true)
let FALSE = boolCheck(false)






console.log("What is TRUE???", TRUE)

console.log("What is FALSE???", FALSE)

console.log("What is TRUE.length ???", TRUE.length)
console.log("What is TRUE(1).length ???", TRUE(1).length)
console.log("What is TRUE(1)(2).length ???", TRUE(1)(2).length)
